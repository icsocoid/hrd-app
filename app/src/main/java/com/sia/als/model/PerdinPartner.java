package com.sia.als.model;

public class PerdinPartner {
    private String id;
    private String nomorPerdin;
    private String statusId;
    private String namaKlien;
    private String jumlahKaryawan;
    private String tanggalAwal;
    private String tanggalAkhir;





    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNomorPerdin() {
        return nomorPerdin;
    }

    public void setNomorPerdin(String nomorPerdin) {
        this.nomorPerdin = nomorPerdin;
    }

    public String getStatusId() {
        return statusId;
    }

    public void setStatusId(String statusId) {
        this.statusId = statusId;
    }

    public String getNamaKlien() {
        return namaKlien;
    }

    public void setNamaKlien(String namaKlien) {
        this.namaKlien = namaKlien;
    }

    public String getJumlahKaryawan() {
        return jumlahKaryawan;
    }

    public void setJumlahKaryawan(String jumlahKaryawan) {
        this.jumlahKaryawan = jumlahKaryawan;
    }

    public String getTanggalAwal() {
        return tanggalAwal;
    }

    public void setTanggalAwal(String tanggalAwal) {
        this.tanggalAwal = tanggalAwal;
    }

    public String getTanggalAkhir() {
        return tanggalAkhir;
    }

    public void setTanggalAkhir(String tanggalAkhir) {
        this.tanggalAkhir = tanggalAkhir;
    }
}
